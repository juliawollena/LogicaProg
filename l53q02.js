// Desenvolver um programa que apresente o total da soma de n números inteiros do número 1 até n, onde n é um
// valor informado pelo usuário.



let num = 8 // numero que o usuario escolheu
let acum= 0 // de onde a soma tem que começar 


console.log (`Informe um número: ${num }`) //exibe o valor que o usuario escolheu

for (cont = 1; cont <= num; cont ++) // da valor ao cont, forma um limete, e adiciona mais um

acum += cont // faz a conta entre o acumulador e o contador

console.log (`A soma dos números é ${acum}`) //exibe a resposta 

















